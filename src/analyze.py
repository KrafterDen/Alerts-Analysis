def plot_duration_distribution(
    selected_oblast: str,
    events: pd.DataFrame,
    figures_dir: Path,
) -> Path:
    path = figures_dir / f"{slugify(selected_oblast)}_alert_duration_distribution.png"
    selected = events[
        (events["oblast_name"] == selected_oblast)
        & events["duration_minutes"].notna()
        & (events["duration_minutes"] >= 0)
    ].copy()
    cap = selected["duration_minutes"].quantile(0.99)
    selected = selected[selected["duration_minutes"] <= cap]

    fig, ax = plt.subplots(figsize=(10, 5))
    sns.histplot(
        data=selected,
        x="duration_minutes",
        bins=50,
        color="#ea580c",
        ax=ax,
    )
    ax.set_title(f"Alert duration distribution: {selected_oblast}")
    ax.set_xlabel("Duration minutes, capped at 99th percentile")
    ax.set_ylabel("Events")
    save_figure(fig, path)
    return path


def save_figure(fig: plt.Figure, path: Path) -> None:
    fig.tight_layout()
    fig.savefig(path, dpi=150)
    plt.close(fig)


def resolve_oblast_name(requested: str, available: list[str]) -> str:
    """Resolve user-friendly oblast aliases to dataset names."""

    if requested in available:
        return requested

    normalized_requested = normalize_lookup(requested)
    alias = OBLAST_ALIASES.get(normalized_requested)
    if alias in available:
        return alias

    normalized_available = {normalize_lookup(name): name for name in available}
    if normalized_requested in normalized_available:
        return normalized_available[normalized_requested]

    stripped_requested = normalized_requested.replace(" oblast", "").replace(" region", "")
    for normalized_name, original_name in normalized_available.items():
        stripped_name = normalized_name.replace(" oblast", "").replace(" region", "")
        if stripped_requested == stripped_name:
            return original_name

    close = ", ".join(available[:12])
    raise ValueError(
        f"Unknown oblast {requested!r}. Available examples: {close}"
    )


def normalize_lookup(value: str) -> str:
    return re.sub(r"\s+", " ", value.strip().casefold())


def slugify(value: str) -> str:
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", value.strip().lower()).strip("_")
    return slug or "selected_oblast"


def run_analysis(
    *,
    oblast: str,
    events_path: Path = EVENTS_PATH,
    daily_path: Path = DAILY_PATH,
    hourly_path: Path = HOURLY_PATH,
) -> AnalysisOutputs:
    """Run the full basic statistical analysis layer."""

    events = load_event_level_alerts(events_path)
    daily = load_daily_timeseries(daily_path)
    hourly = load_hourly_timeseries(hourly_path)

    available_oblasts = sorted(daily["oblast_name"].dropna().unique().tolist())
    selected_oblast = resolve_oblast_name(oblast, available_oblasts)

    oblast_summary = generate_oblast_summary(events, daily)
    daily_stats = generate_time_series_statistics(daily)
    seasonality_summary = generate_seasonality_summary(events, daily, hourly)

    table_paths = save_summary_tables(oblast_summary, seasonality_summary)
    figure_paths = generate_figures(
        selected_oblast=selected_oblast,
        events=events,
        oblast_summary=oblast_summary,
        daily_stats=daily_stats,
        seasonality_summary=seasonality_summary,
    )

    return AnalysisOutputs(
        selected_oblast=selected_oblast,
        oblast_summary=oblast_summary,
        seasonality_summary=seasonality_summary,
        figure_paths=figure_paths,
        table_paths=table_paths,
    )


def print_analysis_summary(outputs: AnalysisOutputs) -> None:
    """Print a compact run summary for CLI use."""

    selected_summary = outputs.oblast_summary[
        outputs.oblast_summary["oblast_name"] == outputs.selected_oblast
    ]

    print("Basic statistical analysis complete")
    print(f"Selected oblast: {outputs.selected_oblast}")
    if not selected_summary.empty:
        print("\nSelected oblast summary")
        print(selected_summary.to_string(index=False))

    print("\nGenerated tables")
    for path in outputs.table_paths:
        print(f"- {path}")

    print("\nGenerated figures")
    for path in outputs.figure_paths:
        print(f"- {path}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run basic statistical analysis for Ukrainian alert data."
    )
    parser.add_argument(
        "--oblast",
        required=True,
        help='Selected oblast for figures, e.g. "Kyiv Oblast" or "Kyiv City".',
    )
    return parser.parse_args()


def main(argv: list[str] | None = None) -> int:
    args = parse_args() if argv is None else parse_args_from(argv)
    outputs = run_analysis(oblast=args.oblast)
    print_analysis_summary(outputs)
    return 0


def parse_args_from(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run basic statistical analysis for Ukrainian alert data."
    )
    parser.add_argument("--oblast", required=True)
    return parser.parse_args(argv)


