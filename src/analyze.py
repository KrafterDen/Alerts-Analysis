@dataclass(frozen=True)
class AnalysisOutputs:
    """Paths and tables generated by an analysis run."""

    selected_oblast: str
    oblast_summary: pd.DataFrame
    seasonality_summary: pd.DataFrame
    figure_paths: list[Path]
    table_paths: list[Path]


def load_event_level_alerts(path: Path = EVENTS_PATH) -> pd.DataFrame:
    """Load event-level merged alerts and parse UTC timestamps."""

    if not path.exists():
        raise FileNotFoundError(
            f"Missing merged event-level alerts: {path}. Run src/merge_sources.py first."
        )

    dataframe = pd.read_csv(path)
    required = {
        "source_record_id",
        "data_source",
        "oblast_name",
        "location_type",
        "alert_type",
        "started_at_utc",
        "finished_at_utc",
        "duration_minutes",
    }
    missing = sorted(required - set(dataframe.columns))
    if missing:
        raise ValueError(f"Event-level alerts missing required columns: {missing}")

    dataframe["started_at_utc"] = pd.to_datetime(
        dataframe["started_at_utc"],
        errors="coerce",
        utc=True,
        format="mixed",
    )
    dataframe["finished_at_utc"] = pd.to_datetime(
        dataframe["finished_at_utc"],
        errors="coerce",
        utc=True,
        format="mixed",
    )
    dataframe["duration_minutes"] = pd.to_numeric(
        dataframe["duration_minutes"],
        errors="coerce",
    )
    return dataframe


def load_daily_timeseries(path: Path = DAILY_PATH) -> pd.DataFrame:
    """Load oblast-level daily time series."""

    if not path.exists():
        raise FileNotFoundError(
            f"Missing daily oblast time series: {path}. "
            "Run src/preprocess_timeseries.py first."
        )

    dataframe = pd.read_csv(path)
    required = {
        "date",
        "oblast_name",
        "alert_count",
        "total_alert_minutes",
        "average_alert_duration",
        "had_alert_binary",
        "github_records_count",
        "api_records_count",
    }
    missing = sorted(required - set(dataframe.columns))
    if missing:
        raise ValueError(f"Daily time series missing required columns: {missing}")

    dataframe["date"] = pd.to_datetime(dataframe["date"], errors="coerce")
    numeric_columns = [
        "alert_count",
        "total_alert_minutes",
        "average_alert_duration",
        "had_alert_binary",
        "github_records_count",
        "api_records_count",
    ]
    for column in numeric_columns:
        dataframe[column] = pd.to_numeric(dataframe[column], errors="coerce")
    return dataframe.dropna(subset=["date", "oblast_name"])


def load_hourly_timeseries(path: Path = HOURLY_PATH) -> pd.DataFrame | None:
    """Load hourly time series when available."""

    if not path.exists():
        return None

    dataframe = pd.read_csv(path)
    if "hour_start_utc" not in dataframe.columns:
        return None

    dataframe["hour_start_utc"] = pd.to_datetime(
        dataframe["hour_start_utc"],
        errors="coerce",
        utc=True,
        format="mixed",
    )
    return dataframe.dropna(subset=["hour_start_utc", "oblast_name"])


def generate_oblast_summary(
    events: pd.DataFrame,
    daily: pd.DataFrame,
) -> pd.DataFrame:
    """Generate summary statistics by oblast."""

    event_summary = (
        events.groupby("oblast_name", dropna=False)
        .agg(
            total_alerts=("source_record_id", "nunique"),
            average_alert_duration=("duration_minutes", "mean"),
            median_alert_duration=("duration_minutes", "median"),
            longest_alert=("duration_minutes", "max"),
        )
        .reset_index()
    )

    daily_summary = (
        daily.groupby("oblast_name", dropna=False)
        .agg(
            total_alert_minutes=("total_alert_minutes", "sum"),
            number_of_days_with_alert=(
                "had_alert_binary",
                lambda values: int((values > 0).sum()),
            ),
        )
        .reset_index()
    )

    summary = event_summary.merge(daily_summary, on="oblast_name", how="outer")
    summary["total_alert_hours"] = summary["total_alert_minutes"] / 60
    summary = summary[
        [
            "oblast_name",
            "total_alerts",
            "total_alert_minutes",
            "total_alert_hours",
            "average_alert_duration",
            "median_alert_duration",
            "longest_alert",
            "number_of_days_with_alert",
        ]
    ]
    return summary.sort_values("total_alert_hours", ascending=False)


def generate_time_series_statistics(daily: pd.DataFrame) -> pd.DataFrame:
    """Add 7-day and 30-day rolling averages to daily time series."""

    sorted_daily = daily.sort_values(["oblast_name", "date"]).copy()
    grouped = sorted_daily.groupby("oblast_name", group_keys=False)
    sorted_daily["alert_count_7d_rolling_avg"] = grouped["alert_count"].transform(
        lambda series: series.rolling(window=7, min_periods=1).mean()
    )
    sorted_daily["alert_count_30d_rolling_avg"] = grouped["alert_count"].transform(
        lambda series: series.rolling(window=30, min_periods=1).mean()
    )
    sorted_daily["alert_minutes_7d_rolling_avg"] = grouped[
        "total_alert_minutes"
    ].transform(lambda series: series.rolling(window=7, min_periods=1).mean())
    sorted_daily["alert_minutes_30d_rolling_avg"] = grouped[
        "total_alert_minutes"
    ].transform(lambda series: series.rolling(window=30, min_periods=1).mean())
    return sorted_daily


def generate_seasonality_summary(
    events: pd.DataFrame,
    daily: pd.DataFrame,
    hourly: pd.DataFrame | None = None,
) -> pd.DataFrame:
    """Generate day-of-week, month, and optional hour-of-day seasonality stats."""

    day_of_week = daily.copy()
    day_of_week["seasonality_type"] = "day_of_week"
    day_of_week["seasonality_value"] = day_of_week["date"].dt.day_name()
    day_summary = aggregate_seasonality(day_of_week)
    day_summary["seasonality_order"] = day_summary["seasonality_value"].map(
        {name: index for index, name in enumerate(DAYS_OF_WEEK, start=1)}
    )

    month = daily.copy()
    month["seasonality_type"] = "month"
    month["seasonality_value"] = month["date"].dt.month_name()
    month_summary = aggregate_seasonality(month)
    month_summary["seasonality_order"] = pd.to_datetime(
        month_summary["seasonality_value"],
        format="%B",
        errors="coerce",
    ).dt.month

    summaries = [day_summary, month_summary]

    if hourly is not None and not hourly.empty:
        hour = hourly.copy()
        hour["seasonality_type"] = "hour_of_day_utc"
        hour["seasonality_value"] = hour["hour_start_utc"].dt.hour.astype("string")
        hour_summary = aggregate_seasonality(hour)
        hour_summary["seasonality_order"] = pd.to_numeric(
            hour_summary["seasonality_value"],
            errors="coerce",
        )
        summaries.append(hour_summary)

    return pd.concat(summaries, ignore_index=True).sort_values(
        ["oblast_name", "seasonality_type", "seasonality_order"]
    )


def aggregate_seasonality(dataframe: pd.DataFrame) -> pd.DataFrame:
    """Aggregate seasonality rows using shared output columns."""

    return (
        dataframe.groupby(
            ["oblast_name", "seasonality_type", "seasonality_value"],
            dropna=False,
        )
        .agg(
            alert_count=("alert_count", "sum"),
            total_alert_minutes=("total_alert_minutes", "sum"),
            average_alert_minutes=("total_alert_minutes", "mean"),
            days_or_hours_observed=("total_alert_minutes", "size"),
        )
        .reset_index()
    )


def save_summary_tables(
    oblast_summary: pd.DataFrame,
    seasonality_summary: pd.DataFrame,
    *,
    tables_dir: Path = TABLES_DIR,
) -> list[Path]:
    """Save summary CSV tables."""

    tables_dir.mkdir(parents=True, exist_ok=True)
    oblast_summary.to_csv(OBLAST_SUMMARY_PATH, index=False)
    seasonality_summary.to_csv(SEASONALITY_SUMMARY_PATH, index=False)
    return [OBLAST_SUMMARY_PATH, SEASONALITY_SUMMARY_PATH]


def generate_figures(
    *,
    selected_oblast: str,
    events: pd.DataFrame,
    oblast_summary: pd.DataFrame,
    daily_stats: pd.DataFrame,
    seasonality_summary: pd.DataFrame,
    figures_dir: Path = FIGURES_DIR,
) -> list[Path]:
    """Generate static Matplotlib/Seaborn figures."""

    figures_dir.mkdir(parents=True, exist_ok=True)
    sns.set_theme(style="whitegrid", context="notebook")

    paths = [
        plot_top_oblasts_by_hours(oblast_summary, figures_dir),
        plot_daily_alert_activity(selected_oblast, daily_stats, figures_dir),
        plot_rolling_average(selected_oblast, daily_stats, figures_dir),
        plot_day_of_week_pattern(selected_oblast, seasonality_summary, figures_dir),
        plot_monthly_pattern(selected_oblast, seasonality_summary, figures_dir),
        plot_duration_distribution(selected_oblast, events, figures_dir),
    ]
    return paths


def plot_top_oblasts_by_hours(summary: pd.DataFrame, figures_dir: Path) -> Path:
    path = figures_dir / "top_oblasts_by_total_alert_hours.png"
    top = summary.nlargest(12, "total_alert_hours").sort_values("total_alert_hours")

    fig, ax = plt.subplots(figsize=(10, 7))
    sns.barplot(
        data=top,
        x="total_alert_hours",
        y="oblast_name",
        color="#2563eb",
        ax=ax,
    )
    ax.set_title("Top oblasts by total alert hours")
    ax.set_xlabel("Total alert hours")
    ax.set_ylabel("")
    save_figure(fig, path)
    return path


def plot_daily_alert_activity(
    selected_oblast: str,
    daily_stats: pd.DataFrame,
    figures_dir: Path,
) -> Path:
    path = figures_dir / f"{slugify(selected_oblast)}_daily_alert_activity.png"
    selected = daily_stats[daily_stats["oblast_name"] == selected_oblast]

    fig, ax = plt.subplots(figsize=(12, 5))
    sns.lineplot(
        data=selected,
        x="date",
        y="total_alert_minutes",
        color="#2563eb",
        linewidth=1,
        ax=ax,
    )
    ax.set_title(f"Daily alert activity: {selected_oblast}")
    ax.set_xlabel("Date")
    ax.set_ylabel("Total alert minutes")
    fig.autofmt_xdate()
    save_figure(fig, path)
    return path


