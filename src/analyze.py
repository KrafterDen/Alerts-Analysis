def plot_day_of_week_pattern(
    selected_oblast: str,
    seasonality: pd.DataFrame,
    figures_dir: Path,
) -> Path:
    path = figures_dir / f"{slugify(selected_oblast)}_day_of_week_pattern.png"
    selected = seasonality[
        (seasonality["oblast_name"] == selected_oblast)
        & (seasonality["seasonality_type"] == "day_of_week")
    ].copy()
    selected["seasonality_value"] = pd.Categorical(
        selected["seasonality_value"],
        categories=DAYS_OF_WEEK,
        ordered=True,
    )
    selected = selected.sort_values("seasonality_value")

    fig, ax = plt.subplots(figsize=(9, 5))
    sns.barplot(
        data=selected,
        x="seasonality_value",
        y="alert_count",
        color="#16a34a",
        ax=ax,
    )
    ax.set_title(f"Alert count by day of week: {selected_oblast}")
    ax.set_xlabel("")
    ax.set_ylabel("Alert count")
    ax.tick_params(axis="x", rotation=35)
    save_figure(fig, path)
    return path


